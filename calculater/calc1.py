#Logic Fuction
	
def backspace():
	global screen
	
	ex=screen.get()
	ex=ex[0:len(ex)-1]
	screen.delete(0,END)
	screen.insert(0,ex)

# Logic of calculator

def click(event):
	global scvalue
	text = event.widget.cget("text")

	if text == "=":
		
		if scvalue.get().isdigit():
			value = int(scvalue.get())
			
		else:
			try:
				value = eval(screen.get())
			except Exception as e:
				print(e)
				value = "Error"
		scvalue.set(value)
		screen.update()
	
	elif text == "Clear":
		scvalue.set("")
		screen.update()
	
	else:
		scvalue.set(scvalue.get() + text)
		screen.update()





